// Prebuild script: parses docs/resume.tex and writes app/lib/resume-text.ts.
// Run automatically via the "prebuild" and "predev" npm hooks.
import { readFileSync, writeFileSync } from "fs";
import { join, dirname } from "path";
import { fileURLToPath } from "url";

const root = join(dirname(fileURLToPath(import.meta.url)), "..");

function parseResumeText(raw) {
  return raw
    .replace(/\\documentclass[\s\S]*?\\begin\{document\}/, "")
    .replace(/\\end\{document\}/, "")
    .replace(/\\textbf\{([^}]*)\}/g, "$1")
    .replace(/\\textit\{([^}]*)\}/g, "$1")
    .replace(/\\emph\{([^}]*)\}/g, "$1")
    .replace(/\\href\{[^}]*\}\{([^}]*)\}/g, "$1")
    .replace(/\\section\{([^}]*)\}/g, "\n\n=== $1 ===\n")
    .replace(/\\item\s*/g, "\n- ")
    .replace(/\\\\/g, "\n")
    .replace(/\\hfill/g, "  |  ")
    .replace(/\\vspace\{[^}]*\}/g, "\n")
    .replace(/\\noindent\s*/g, "")
    .replace(/\\begin\{[^}]*\}/g, "")
    .replace(/\\end\{[^}]*\}/g, "")
    .replace(/\\&/g, "&")
    .replace(/\$\s*\|\s*\$/g, " | ")
    .replace(/\\[a-zA-Z]+\{([^}]*)\}/g, "$1")
    .replace(/\\[a-zA-Z]+(\[[^\]]*\])?\s*/g, "")
    .replace(/[{}]/g, "")
    .replace(/\$[^$]*\$/g, "")
    .replace(/\n{3,}/g, "\n\n")
    .trim();
}

const raw = readFileSync(join(root, "docs", "resume.tex"), "utf-8");
const text = parseResumeText(raw);

const output =
  `// Auto-generated by scripts/build-resume-text.mjs — do not edit manually.\n` +
  `export const RESUME_TEXT = ${JSON.stringify(text)};\n`;

writeFileSync(join(root, "app", "lib", "resume-text.ts"), output);
console.log("Resume text written to app/lib/resume-text.ts");
